-- NPC conversation memory (last 3 summaries per player-NPC pair)
CREATE TABLE IF NOT EXISTS npc_interactions (
  player_id  UUID        NOT NULL REFERENCES players(id) ON DELETE CASCADE,
  npc_id     VARCHAR(50) NOT NULL,
  zone_id    VARCHAR(50) NOT NULL,
  -- JSON array of up to 3 interaction summaries (most recent last)
  summaries  JSONB       NOT NULL DEFAULT '[]',
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  PRIMARY KEY (player_id, npc_id)
);

-- Seasons (active season drives LLM quest theme)
CREATE TABLE IF NOT EXISTS seasons (
  id                    UUID        DEFAULT gen_random_uuid() PRIMARY KEY,
  name                  VARCHAR(60) NOT NULL,
  start_date            DATE        NOT NULL,
  end_date              DATE        NOT NULL,
  story_prompt_template TEXT        NOT NULL DEFAULT '',
  is_active             BOOLEAN     NOT NULL DEFAULT FALSE,
  created_at            TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
CREATE INDEX IF NOT EXISTS seasons_active_idx ON seasons(is_active) WHERE is_active = TRUE;

