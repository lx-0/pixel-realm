-- World events (persisted across restarts)
CREATE TABLE IF NOT EXISTS world_events (
  id          UUID        DEFAULT gen_random_uuid() PRIMARY KEY,
  zone_id     VARCHAR(50) NOT NULL,
  name        VARCHAR(100) NOT NULL,
  description TEXT         NOT NULL DEFAULT '',
  is_active   BOOLEAN      NOT NULL DEFAULT TRUE,
  event_data  JSONB        NOT NULL DEFAULT '{}',
  starts_at   TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
  ends_at     TIMESTAMPTZ,
  created_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
  updated_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW()
);
CREATE INDEX IF NOT EXISTS world_events_zone_active_idx ON world_events(zone_id, is_active);

-- Quest chains (multi-step storylines generated by LLM)
CREATE TABLE IF NOT EXISTS quest_chains (
  id                   UUID        DEFAULT gen_random_uuid() PRIMARY KEY,
  zone_id              VARCHAR(50) NOT NULL,
  player_level_bucket  INTEGER     NOT NULL,
  title                VARCHAR(200) NOT NULL,
  theme                TEXT         NOT NULL DEFAULT '',
  -- JSON array of quest IDs referencing generated_quests
  quest_ids            JSONB        NOT NULL DEFAULT '[]',
  generated_at         TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
  expires_at           TIMESTAMPTZ  NOT NULL
);
CREATE INDEX IF NOT EXISTS quest_chains_zone_bucket_idx ON quest_chains(zone_id, player_level_bucket);

-- Track player progress through quest chains
CREATE TABLE IF NOT EXISTS player_chain_progress (
  player_id    UUID        NOT NULL REFERENCES players(id) ON DELETE CASCADE,
  chain_id     UUID        NOT NULL REFERENCES quest_chains(id) ON DELETE CASCADE,
  current_step INTEGER     NOT NULL DEFAULT 0,
  status       VARCHAR(20) NOT NULL DEFAULT 'active', -- 'active' | 'completed' | 'abandoned'
  started_at   TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at   TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  PRIMARY KEY (player_id, chain_id)
);
