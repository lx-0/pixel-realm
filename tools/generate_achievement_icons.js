function parseGrid(template) {
  const rows = template.trim().split('\n').map(r => r.trim());
  if (rows.length !== 16) throw new Error(`Expected 16 rows, got ${rows.length}`);
  for (let i = 0; i < 16; i++) {
    if (rows[i].length !== 16) {
      throw new Error(`Row ${i} is ${rows[i].length} chars (expected 16): "${rows[i]}"`);
    }
    for (let j = 0; j < 16; j++) {
      if (!PAL.hasOwnProperty(rows[i][j])) {
        throw new Error(`Unknown char '${rows[i][j]}' at row ${i} col ${j}`);
      }
    }
  }
  return rows;
}

function gridToBuffer(rows) {
  const buf = Buffer.alloc(16 * 16 * 4, 0);
  for (let y = 0; y < 16; y++) {
    for (let x = 0; x < 16; x++) {
      const rgba = PAL[rows[y][x]];
      if (rgba[3] === 0) continue;
      const off = (y * 16 + x) * 4;
      buf[off]     = rgba[0];
      buf[off + 1] = rgba[1];
      buf[off + 2] = rgba[2];
      buf[off + 3] = rgba[3];
    }
  }
  return buf;
}

