// ================================================================
// GENERATION
// ================================================================

const OUT_DIR = path.resolve(__dirname, '..', 'assets', 'ui', 'icons');

async function main() {
  console.log('Generating M37-M40 achievement icons...');
  console.log(`Output: ${OUT_DIR}\n`);

  const names = Object.keys(ICONS);
  const buffers = [];

  for (const name of names) {
    const grid = parseGrid(ICONS[name]);
    const buf = gridToBuffer(grid);
    const file = path.join(OUT_DIR, `icon_achieve_${name}.png`);
    await sharp(buf, { raw: { width: 16, height: 16, channels: 4 } })
      .png()
      .toFile(file);
    console.log(`  ✓ icon_achieve_${name}.png`);
    buffers.push({ name, buf });
  }

  // Assemble sprite sheet: 8 columns × 3 rows
  const cols = 8;
  const rows = Math.ceil(names.length / cols);
  const sheetW = cols * 16;
  const sheetH = rows * 16;
  const sheetBuf = Buffer.alloc(sheetW * sheetH * 4, 0);

  for (let idx = 0; idx < buffers.length; idx++) {
    const col = idx % cols;
    const row = Math.floor(idx / cols);
    const src = buffers[idx].buf;
    for (let y = 0; y < 16; y++) {
      for (let x = 0; x < 16; x++) {
        const srcOff = (y * 16 + x) * 4;
        const dstX = col * 16 + x;
        const dstY = row * 16 + y;
        const dstOff = (dstY * sheetW + dstX) * 4;
        sheetBuf[dstOff]     = src[srcOff];
        sheetBuf[dstOff + 1] = src[srcOff + 1];
        sheetBuf[dstOff + 2] = src[srcOff + 2];
        sheetBuf[dstOff + 3] = src[srcOff + 3];
      }
    }
  }

  const sheetFile = path.join(OUT_DIR, 'icon_achieve_m37m40_sheet.png');
  await sharp(sheetBuf, { raw: { width: sheetW, height: sheetH, channels: 4 } })
    .png()
    .toFile(sheetFile);

  console.log(`\n  ✓ Sprite sheet: icon_achieve_m37m40_sheet.png (${sheetW}×${sheetH}, ${cols}×${rows} grid)`);
  console.log(`\nSprite sheet layout (left→right, top→bottom):`);
  for (let r = 0; r < rows; r++) {
    const rowNames = names.slice(r * cols, (r + 1) * cols);
    console.log(`  Row ${r}: ${rowNames.join(', ')}`);
  }

  console.log(`\nDone! ${names.length} icons + 1 sprite sheet generated.`);
}

main().catch(err => {
  console.error('ERROR:', err.message);
  process.exit(1);
});
